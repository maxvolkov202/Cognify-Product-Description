#!/usr/bin/env node
/**
 * Pre-build step: reads all markdown files under src/lib/ai/knowledge/ and
 * emits src/lib/ai/knowledge/generated.ts as plain TypeScript string constants.
 *
 * Why: in production (Vercel serverless), fs.readFileSync against
 * src/lib/ai/knowledge/*.md silently returns empty because the markdown
 * files aren't reliably bundled with the serverless function. Embedding
 * them as TS string constants at build time guarantees they ship.
 *
 * Idempotent — safe to run repeatedly. Output file is committed to git.
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..", "src", "lib", "ai", "knowledge");
const OUT = join(ROOT, "generated.ts");

const SUBDIRS = ["frameworks", "skills", "domains", "patterns", "progression"];

/** @type {Record<string, Array<{id: string, content: string}>>} */
const collected = {};

for (const sub of SUBDIRS) {
  const dir = join(ROOT, sub);
  try {
    const files = readdirSync(dir).filter(
      (f) => f.endsWith(".md") && f.toLowerCase() !== "readme.md",
    );
    collected[sub] = files.map((file) => ({
      id: file.replace(/\.md$/, ""),
      content: readFileSync(join(dir, file), "utf-8"),
    }));
  } catch (e) {
    console.warn(`[build-knowledge] skipping ${sub}:`, e.message);
    collected[sub] = [];
  }
}

const banner = `// AUTO-GENERATED by scripts/build-knowledge.mjs. Do not edit manually.
// Source: src/lib/ai/knowledge/{frameworks,skills,domains,patterns,progression}/*.md
// Run \`npm run build:knowledge\` to regenerate.

`;

const body = SUBDIRS.map((sub) => {
  const upper = sub.toUpperCase();
  const entries = collected[sub]
    .map(
      (b) =>
        `  { id: ${JSON.stringify(b.id)}, content: ${JSON.stringify(b.content)} }`,
    )
    .join(",\n");
  return `export const ${upper}_BLOCKS: ReadonlyArray<{ id: string; content: string }> = [\n${entries}\n];`;
}).join("\n\n");

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, banner + body + "\n", "utf-8");

const counts = SUBDIRS.map((s) => `${s}=${collected[s].length}`).join(", ");
console.log(`[build-knowledge] wrote ${OUT} (${counts})`);
