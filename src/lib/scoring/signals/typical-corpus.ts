/** Lookup document frequency. Tokens not in the map default to 0.8
 *  (assume common — neutral originality contribution). */
export function lookupDocumentFrequency(token: string): number {
  if (!token) return 0.8;
  const lower = token.toLowerCase();
  if (lower in TYPICAL_DOCUMENT_FREQUENCY) {
    return TYPICAL_DOCUMENT_FREQUENCY[lower]!;
  }
  // Pluralization: "platforms" → "platform"
  if (lower.endsWith("s") && lower.length > 3) {
    const stem = lower.slice(0, -1);
    if (stem in TYPICAL_DOCUMENT_FREQUENCY) {
      return TYPICAL_DOCUMENT_FREQUENCY[stem]!;
    }
  }
  if (lower.endsWith("es") && lower.length > 4) {
    const stem = lower.slice(0, -2);
    if (stem in TYPICAL_DOCUMENT_FREQUENCY) {
      return TYPICAL_DOCUMENT_FREQUENCY[stem]!;
    }
  }
  // -ing → bare
  if (lower.endsWith("ing") && lower.length > 5) {
    const stem = lower.slice(0, -3);
    if (stem in TYPICAL_DOCUMENT_FREQUENCY) {
      return TYPICAL_DOCUMENT_FREQUENCY[stem]!;
    }
  }
  // -ed → bare
  if (lower.endsWith("ed") && lower.length > 4) {
    const stem = lower.slice(0, -2);
    if (stem in TYPICAL_DOCUMENT_FREQUENCY) {
      return TYPICAL_DOCUMENT_FREQUENCY[stem]!;
    }
  }
  return 0.8;
}
