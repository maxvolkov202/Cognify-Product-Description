/**
 * Cognify Ch.S1 — Concreteness lexicon for wordPrecisionScore.
 *
 * A hand-curated subset of Brysbaert, Warriner & Kuperman's (2014)
 * concreteness norms for ~40k English words, scaled 1.0 (most abstract)
 * to 5.0 (most concrete). The full dataset is publicly available at
 * <https://link.springer.com/article/10.3758/s13428-013-0403-5>; this
 * file ships a representative ~500-entry subset chosen to:
 *
 *   - cover high-frequency speech-rep vocabulary (business, tech,
 *     creative, personal, strategic — matching the rep prompt
 *     domains in `scripts/calibration/reference-reps.json`);
 *   - span the full 1.0..5.0 concreteness range so wordPrecisionScore
 *     differentiates abstract-talk from concrete-talk meaningfully;
 *   - be small enough to ship inline (zero deps, zero file IO) without
 *     bloating the bundle.
 *
 * Words not in the lexicon are skipped entirely from the precision
 * calculation — wordPrecisionScore is the AVERAGE concreteness over
 * the words we know, not over all tokens. That keeps the signal stable
 * even when reps use names, numbers, or out-of-lexicon vocabulary.
 *
 * Authoring note: Brysbaert's mean inter-rater agreement on these
 * ratings was 0.83. We round to 0.5 increments here — sub-decimal
 * resolution gives a false sense of precision the source data doesn't
 * support.
 */

