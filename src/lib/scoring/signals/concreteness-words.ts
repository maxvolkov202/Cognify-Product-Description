/** Lookup a token's concreteness rating. Returns null if the lemma is
 *  not in the lexicon (caller should skip — see file-level comment).
 *  Handles common pluralization (trailing -s, -es) and -ing/-ed verb
 *  endings via a small set of normalization rules. */
export function lookupConcreteness(token: string): number | null {
  if (!token) return null;
  const lower = token.toLowerCase();
  if (lower in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[lower]!;
  // -es plural (tomatoes → tomato)
  if (lower.endsWith("es") && lower.length > 4) {
    const stem = lower.slice(0, -2);
    if (stem in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[stem]!;
  }
  // -s plural (cars → car)
  if (lower.endsWith("s") && lower.length > 3) {
    const stem = lower.slice(0, -1);
    if (stem in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[stem]!;
  }
  // -ing form (running → run) — only matches if removing -ing gives a
  // valid lemma; skips false positives like "thing" → "th".
  if (lower.endsWith("ing") && lower.length > 5) {
    const stem = lower.slice(0, -3);
    if (stem in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[stem]!;
    // doubled-consonant case (running → run)
    if (stem.length > 1 && stem[stem.length - 1] === stem[stem.length - 2]) {
      const dedup = stem.slice(0, -1);
      if (dedup in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[dedup]!;
    }
  }
  // -ed past tense (worked → work)
  if (lower.endsWith("ed") && lower.length > 4) {
    const stem = lower.slice(0, -2);
    if (stem in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[stem]!;
    const stem1 = lower.slice(0, -1);
    if (stem1 in CONCRETENESS_LEXICON) return CONCRETENESS_LEXICON[stem1]!;
  }
  return null;
}
